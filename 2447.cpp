#include <iostream>

void determine(int i, int j, int n)
{
	if ((i / n) % 3 == 1 && (j / n) % 3 == 1)
		std::cout << ' ';
	else 
	{
		if (n / 3 == 0)
			std::cout << '*';
		else
			determine(i, j, n / 3);
	}
}

int main()
{
	int n = 0;
	std::cin >> n;
	for (auto i = 0; i < n; ++i)
	{
		for (auto j = 0; j < n; ++j)
		{
			determine(i, j, n);
		}
		std::cout << '\n';
	}
}

